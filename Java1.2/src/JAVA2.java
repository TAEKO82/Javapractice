class Cup{

	int mizu = 0;
	int sokudo = 0;
	
	public void sosogu(int L) {
		//水をmizuにint L注ぐ。mizu+L
		mizu = mizu + L;
	}
	
	public int mazeru(int s) {
		return sokudo = s;
	}
	
	public void tomaru(String t) {
		//止まれと言われたら止まる。sokudoが０になる。
		//引数にtomareが入ると、sokudoが０になる。t = "tomare";
		if (t == "tomare") {
			sokudo = 0;
		}	
		
	}
	public void nomu(int L) {
		//水をL飲む。mizu-L
		mizu = mizu - L;
	}
	
}

	

